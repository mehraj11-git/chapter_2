language= "python and java"
# we use this method to access particular word by giving its index number 
# index number always start with 0 and -1 from last 
print(language[4])
# indexing from last 
print(language[-8])
# string slicing use to print more than one character 
# we use start argument to stop argument
print(language [4:6])
# we can stop the string by this [0:]
print(language[0:])
# you will get full string by this [:]
print(language[:])

# step in range func
for i in range(1,-12,-1):
    print(i)